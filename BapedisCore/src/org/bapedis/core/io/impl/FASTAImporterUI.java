    private void updateValidState() {
        boolean oldValidState = validState;
        if (jTF_file.getText().isEmpty() ||
            jTF_workspace.getText().isEmpty()) {
            validState = false;
        }else{
            validState = true;
        }
        changeSupport.firePropertyChange(VALID_STATE, oldValidState, validState);
    }

