    @Override
    public boolean isValidState() {
        if (jTF_file.getText().isEmpty()) {
            return false;
        }
        if (jTF_workspace.getText().isEmpty()) {
            return false;
        }
        return validState;
    }

    @Override
    public void saveSettings() {
        selectedFile = new File(jTF_file.getText());
        parentDirectory = selectedFile.getParentFile();
    }

    @Override
    public void cancelSettings() {
        selectedFile = null;
    }

    @Override
    public void addValidStateListener(PropertyChangeListener listener) {
        changeSupport.addPropertyChangeListener(listener);
    }

    @Override
    public void removeValidStateListener(PropertyChangeListener listener) {
        changeSupport.removePropertyChangeListener(listener);
    }
