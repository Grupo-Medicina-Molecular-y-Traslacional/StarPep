    @Override
    public JPanel getSettingPanel(Algorithm algo) {
        embeddingInput = (EmbeddingInputSeqAlg) algo;
        
        switch(embeddingInput.getNrdOption()){
            case NO:
                jOption1.setSelected(true);
                break;
            case YES:
                jOption2.setSelected(true);
                break;
        }
        
        NonRedundantSetAlg nrdAlg = embeddingInput.getNonRedundantAlg();
        nrdPanel = nrdAlg.getFactory().getSetupUI().getSettingPanel(nrdAlg);
        
        centerPanel.removeAll();
        centerPanel.add(nrdPanel, BorderLayout.CENTER);
        centerPanel.revalidate();
        centerPanel.repaint();
        
        return this;
    }


