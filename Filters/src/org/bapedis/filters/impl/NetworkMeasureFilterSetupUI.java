    private void attrComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_attrComboBoxActionPerformed
        opComboBox.removeAllItems();
        if (attrComboBox.getSelectedItem() instanceof ComboBoxItem) {
            ComboBoxItem item = (ComboBoxItem) attrComboBox.getSelectedItem();
            FilterOperator[] operators = FilterHelper.getOperators(item.getColumn().getTypeClass());
            for (FilterOperator operator : operators) {
                opComboBox.addItem(operator);
            }
        }
    }//GEN-LAST:event_attrComboBoxActionPerformed

    @Override
    public JPanel getEditPanel(Filter filter) {
        this.filter = (NetworkMeasureFilter) filter;
        notCheckBox.setSelected(this.filter.isNegative());
        initAttrComboBox();
        Column attr = this.filter.getAttribute();
        ComboBoxItem item = (ComboBoxItem) attrComboBox.getSelectedItem();
        if (attr != null && !attr.equals(item.getColumn())) {
            for (int i = 0; i < attrComboBox.getItemCount(); i++) {
                item = (ComboBoxItem) attrComboBox.getItemAt(i);
                if (item.getColumn() == attr) {
                    attrComboBox.setSelectedItem(item);
                }
            }
        }
        FilterOperator operator = this.filter.getOperator();
        if (operator != null && !operator.equals(opComboBox.getSelectedItem())) {
            opComboBox.setSelectedItem(operator);
        }
        String value = this.filter.getValue() != null ? this.filter.getValue() : "";
        if (!value.equals(valueTextField.getText())) {
            valueTextField.setText(value);
        } else {
            updateValidState();
        }
        return this;
    }

    @Override
    public void saveSettings() {
        FilterOperator operator = (FilterOperator) opComboBox.getSelectedItem();
        String operand = valueTextField.getText();
        if (operator.isValid(operand)) {
            filter.setNegative(notCheckBox.isSelected());
            filter.setAttribute(((ComboBoxItem) attrComboBox.getSelectedItem()).getColumn());
            filter.setOperator((FilterOperator) opComboBox.getSelectedItem());
            filter.setValue(valueTextField.getText().trim());
        }

    }

    @Override
    public void cancelSettings() {
        filter = null;
    }

