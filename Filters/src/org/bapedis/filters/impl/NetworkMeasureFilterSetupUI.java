    private void updateValidState() {
        boolean oldValue = validState;
        validState = true;
        errorLabel.setText("");
        if (attrComboBox.getSelectedIndex() == -1) {
            validState = false;
            errorLabel.setText(NbBundle.getMessage(NetworkMeasureFilterSetupUI.class, "NetworkMeasureFilterSetupUI.errorLabel.invalidAttribute"));
        }
        if (validState && opComboBox.getSelectedIndex() == -1) {
            validState = false;
            errorLabel.setText(NbBundle.getMessage(NetworkMeasureFilterSetupUI.class, "NetworkMeasureFilterSetupUI.errorLabel.invalidOperator"));
        }
        FilterOperator operator = (FilterOperator) opComboBox.getSelectedItem();
        if (validState && (valueTextField.getText().isEmpty()
                || ((operator instanceof IntegerFilterOperator
                || operator instanceof LongFilterOperator
                || operator instanceof DoubleFilterOperator)
                && !operator.isValid(valueTextField.getText())))) {
            validState = false;
            errorLabel.setText(NbBundle.getMessage(NetworkMeasureFilterSetupUI.class, "NetworkMeasureFilterSetupUI.errorLabel.invalidValue"));
        }
        changeSupport.firePropertyChange(VALID_STATE, oldValue, validState);
    }

    protected void initAttrComboBox() {
        attrComboBox.removeAllItems();
        Table nodeTable = pc.getGraphModel().getNodeTable();
        for (Column column : nodeTable) {
            if (column.getOrigin() == Origin.DATA) {
                attrComboBox.addItem(new ComboBoxItem(column));
            }
        }
        if (attrComboBox.getItemCount() > 0) {
            attrComboBox.setSelectedIndex(0);
        }
    }

